gapPenalty = -5

def editDistance(s, t):
    ls = len(s) + 1
    lt = len(t) + 1
    mat = [ [0 for j in range(0,lt)]  for i in range(0,ls)]
    for i in range(1,ls):
        mat[i][0] = i * gapPenalty
    for j in range(1,lt):
        mat[0][j] = j * gapPenalty
    for i in range(1,ls):
        sa = s[i-1]
        for j in range(1,lt):
            ta = t[j-1]
            scoreVal = score[syms[sa]][syms[ta]]
            if sa == ta:
                mat[i][j] = mat[i-1][j-1] + scoreVal
            else:
                a = mat[i-1][j] + gapPenalty
                b = mat[i][j-1] + gapPenalty
                c = mat[i-1][j-1] + scoreVal
                mat[i][j] = max(a, b, c)
    return mat[-1][-1]

